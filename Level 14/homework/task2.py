# მეტი (>)
print(5 > 3)     # True
print(10 > 20)   # False
print(-1 > -5)   # True
print(7.5 > 7.4) # True
print(0 > 0)     # False

# მეტია ან ტოლია (>=)
print(5 >= 3)    # True
print(10 >= 10)  # True
print(-1 >= 0)   # False
print(4.5 >= 4.5)# True
print(2 >= 3)    # False

# ნაკლები (<)
print(5 < 3)     # False
print(10 < 20)   # True
print(-1 < -5)   # False
print(7.5 < 7.6) # True
print(0 < 0)     # False

# ნაკლები ან ტოლია (<=)
print(5 <= 3)    # False
print(10 <= 10)  # True
print(-1 <= 0)   # True
print(4.5 <= 4.6)# True
print(2 <= 1)    # False

# ტოლია (==)
print(5 == 3)    # False
print(10 == 10)  # True
print(-1 == -1)  # True
print(4.5 == 4.5)# True
print(2 == 3)    # False

# არ ტოლია (!=)
print(5 != 3)    # True
print(10 != 10)  # False
print(-1 != 0)   # True
print(4.5 != 4.6)# True
print(2 != 2)    # False
