#3) შექმენით 3 ცვლადი რომლებშიც შეინახავთ რიცხვით integer მნიშვნელობებს, შემდეგ სამივეს შორის დაბეჭდეთ შემდეგი ოპერაციების (+,-,*,/) შედეგები
# სამი integer ცვლადის შექმნა
a = 15
b = 5
c = 3

print("ჯამი (a + b + c):", a + b + c)        # 15 + 5 + 3 = 23
print("სხვაობა (a - b - c):", a - b - c)    # 15 - 5 - 3 = 7
print("ნამრავლი (a * b * c):", a * b * c)   # 15 * 5 * 3 = 225
print("გაყოფა (a / b / c):", a / b / c)     # 15 / 5 / 3 = 1.0
