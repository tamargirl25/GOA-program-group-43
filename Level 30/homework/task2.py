total = 0  # ჯამის ინიციალიზაცია

while True:
    number = int(input("შეიტანეთ რიცხვი: "))  # მომხმარებლის მიერ შეყვანილი რიცხვი
    
    if number < 0:
        break  # თუ რიცხვი უარყოფითია, დაასრულეთ ციკლი
    else:
        total += number  # თუ რიცხვი დადებითია, დაამატეთ ჯამში

print(f"დადებითი რიცხვების ჯამი: {total}")

