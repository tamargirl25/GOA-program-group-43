total = 0  # ჯამი
count = 0  # რიცხვების რაოდენობა

while True:
    number = int(input("შეიტანეთ რიცხვი (ან -1 რათა შეწყვიტოთ): "))  # მომხმარებლის შეყვანილი რიცხვი
    
    if number == -1:
        break  # თუ რიცხვი -1 არის, დასრულდება ციკლი
    
    total += number  # რიცხვი ემატება ჯამში
    count += 1  # რიცხვების რაოდენობა იზრდება

if count > 0:
    average = total / count  # საშუალო
    print(f"შეყვანილი რიცხვების საშუალო: {average}")
else:
    print("არ შევიდა რიცხვები.")
