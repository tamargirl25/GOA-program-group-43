even_count = 0  # ლუწი რიცხვების რაოდენობის ინიციალიზაცია
odd_count = 0   # კენტი რიცხვების რაოდენობის ინიციალიზაცია

while True:
    number = int(input("შეიტანეთ რიცხვი: "))  # მომხმარებლის მიერ შეყვანილი რიცხვი
    
    if number < 0:
        break  # თუ რიცხვი უარყოფითია, დაასრულეთ ციკლი
    
    if number % 2 == 0:
        even_count += 1  # თუ რიცხვი ლუწია, გაზარდეთ ლუწი რიცხვების რაოდენობა
    else:
        odd_count += 1   # თუ რიცხვი კენტია, გაზარდეთ კენტი რიცხვების რაოდენობა

print(f"ლუწი რიცხვების რაოდენობა: {even_count}")
print(f"კენტი რიცხვების რაოდენობა: {odd_count}")

