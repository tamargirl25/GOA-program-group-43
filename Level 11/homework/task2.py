#HTML სტრუქტურა შედგება სამი ძირითადი ნაწილისგან:

#<html> - მთავარი კონტეინერი, რომელიც მოიცავს მთელ ვებგვერდს.
#<head> - ის შეიცავს მეტამონაცემებს, სათაურს, სტილებს და სკრიპტებს.
#<body> - აქ განთავსებულია უშუალოდ შიგთავსი (ტექსტი, სურათები, ბმულები და სხვა ელემენტები).

#თქვენ შეგიძლიათ დახატოთ ეს სტრუქტურა Paint-ში შემდეგნაირად:

#დახაზეთ დიდი მართკუთხედი, რომელიც წარმოადგენს <html> ტეგს.
#შიგნით ორი smaller მართკუთხედი: ერთს დააწერეთ <head>, მეორეს <body>.
#<head> მდებარეობს ზედა ნაწილში და შედარებით პატარა უნდა იყოს.
#<body> დიდი უნდა იყოს და მოიცავდეს გვერდის ძირითად შიგთავსს.
