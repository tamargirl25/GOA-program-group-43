#4) კომენტარების საშვალებით აღწერეთ ყველა შესრულებული დავალება, ასევე დაწერეთ როგორ გესმით მონაცემთა ტიპი
#მონაცემთა ტიპი არის მონაცემების კლასიფიკაცია, რომელიც განსაზღვრავს, თუ როგორ უნდა იქნას შენახული და დამუშავებული ეს მოცემები.
#python-ში ყველაზე გავრცელებული მონაცემთა ტიპებია:
# integer (int) - მთელი რიცხვები, როგორიცაა 5, -10, 100
# Float (float) - ათწილადი რიცხვები, როგორიცაა 3.14, -0,5
# string (str) - ტექსტური მონაცემები, როგორიცაა "Hello", "Python"
# boolean (bool) - ლოგიკური მნიშვნელობები: True ან False

#  # შეკრება (+) - ორი რიცხვის ჯამი
print(10 + 5)  # 10-ს და 5-ს ვუმატებთ
print(20 + 7)  # 20-ს და 7-ს ვუმატებთ
print(35 + 15) # 35-ს და 15-ს ვუმატებთ
print(8 + 12)  # 8-ს და 12-ს ვუმატებთ
print(50 + 25) # 50-ს და 25-ს ვუმატებთ

# გამოკლება (-) - ერთი რიცხვის შემცირება მეორისგან
print(30 - 10)  # 30-დან ვაკლებთ 10-ს
print(100 - 45) # 100-დან ვაკლებთ 45-ს
print(55 - 20)  # 55-დან ვაკლებთ 20-ს
print(90 - 35)  # 90-დან ვაკლებთ 35-ს
print(60 - 15)  # 60-დან ვაკლებთ 15-ს

# გამრავლება (*) - ორი რიცხვის ნამრავლი
print(5 * 4)   # 5 გამრავლებული 4-ზე
print(12 * 3)  # 12 გამრავლებული 3-ზე
print(7 * 8)   # 7 გამრავლებული 8-ზე
print(6 * 9)   # 6 გამრავლებული 9-ზე
print(11 * 2)  # 11 გამრავლებული 2-ზე

# მთელ რიცხვზე გაყოფა (//) - რიცხვის გაყოფა ისე, რომ ვიღებთ მხოლოდ მთელ ნაწილს
print(50 // 5)  # 50 გაყოფილი 5-ზე
print(100 // 4) # 100 გაყოფილი 4-ზე
print(75 // 3)  # 75 გაყოფილი 3-ზე
print(90 // 6)  # 90 გაყოფილი 6-ზე
print(40 // 8)  # 40 გაყოფილი 8-ზე
